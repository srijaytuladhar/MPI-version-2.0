unsigned char image_data[], results[img_width * img_height];
int startingIndex, endingIndex;


void detect_image_edges(unsigned char *input, unsigned char *output) {
  int loop;
  int no_of_pixels = img_width * img_height; //we can get total pixels when width is multiplied by height

  for(loop=0; loop<no_of_pixels; loop++) {
    int x_pix, y_pix; // the pixel of interest
    int pix_b, pix_d, pix_f, pix_h; // the pixels adjacent to x,y used for the calculation
    int res; // the result of calculate
    
    y_pix = loop / img_width; // convolution logic
    x_pix = loop - (img_width * y_pix); // convolution logic

    if (x_pix == 0 || y_pix == 0 || x_pix == img_width - 1 || y_pix == img_height - 1) {
      results[loop] = 0; //when x=0 and y=0 result is 0
    } else {
      pix_b = loop + img_width; //taking value of all axis
      pix_d = loop - 1;
      pix_f = loop + 1;
      pix_h = loop - img_width;

      res = (input[loop] * 4) + (input[pix_b] * -1) + (input[pix_d] * -1) + (input[pix_f] * -1)
          + (input[pix_h] * -1); // formula

      if (res > 0) { // if the result is positive this is an edge pixel
        output[loop] = 255;
      } else {
        output[loop] = 0;
      } 
    }
  }
}

void tidy_and_exit() {
  exit(0);
}

void sigint_callback(int signal_number){
  printf("\nInterrupt from keyboard\n");
  tidy_and_exit();
}

static void display() {
  glClear(GL_COLOR_BUFFER_BIT);
  glRasterPos4i(-1, -1, 0, 1);
  glDrawPixels(img_width, img_height, GL_LUMINANCE, GL_UNSIGNED_BYTE, image_data);
  glRasterPos4i(0, -1, 0, 1);
  glDrawPixels(img_width, img_height, GL_LUMINANCE, GL_UNSIGNED_BYTE, results);
  glFlush();
}

static void key_pressed(unsigned char key, int x, int y) {
  switch(key){
    case 27: // escape
      tidy_and_exit();
      break;
    default: 
      printf("\nPress escape to exit\n");
      break;
  }
}

