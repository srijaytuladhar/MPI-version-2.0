typedef struct plot_coordinate {
  double x;		// x co-ordinate
  double y;		// y co-ordinate
} plot_coordinate;

int no_of_data = 1000;
plot_coordinate data[];

double residual_error(double x, double y, double m, double c) {
  double e = (m * x) + c - y;
  return e * e;
}

double rms_error_calc(double m, double c) {
  int loop;		// counter
  double mean_value;
  double error_sum = 0;
  
  for(loop=0; loop<no_of_data; loop++) {
    error_sum += residual_error(data[loop].x, data[loop].y, m, c);  
  }
  
  mean_value = error_sum / no_of_data;
  
  return sqrt(mean_value);
}

/* main function */
int main() {

 // initializing timer.c components
  struct timespec timer_start, timer_stop;   
  long long int time_taken_for_execution;
  
  // timer starts here
  clock_gettime(CLOCK_MONOTONIC, &timer_start);
  printf("\n===============================================================================\n");
  printf("!! OUTPUT FOR LINEAR REGRESSION !! \n");
  printf("===============================================================================\n\n");

  int mpi_size, mpi_rank;		// size and rank for sender and receiver
  int loop;						// counter
  double m_base = 1.3;
  double c_base = 10;
  double e_base;
  double m[8];
  double c[8];
  double e[8];
  double step = 0.01;
  double best_error = 999999999;
  int best_error_i;
  int minimum_found = 0;
  
  double pError = 0;
  double baseMC[2];
  
  double m_origin[] = {0,1,1, 1, 0,-1,-1,-1};
  double c_origin[] = {1,1,0,-1,-1,-1, 0, 1};

  MPI_Init(NULL, NULL);
  MPI_Comm_size(MPI_COMM_WORLD, &mpi_size);
  MPI_Comm_rank(MPI_COMM_WORLD, &mpi_rank);

  e_base = rms_error_calc(m_base, c_base);

  if(mpi_size != 9) {
    	if(mpi_rank == 0) {
      		printf("This program needs to run on exactly 9 processes\n");
      		return 0;
     	}
  } 

  while(!minimum_found) {
    	if (mpi_rank != 0) {
			loop = mpi_rank -1;
			m[loop] = m_base + (m_origin[loop] * step);
			c[loop] = c_base + (c_origin[loop] * step);
			pError = rms_error_calc (m[loop], c[loop]);

			MPI_Send (&pError, 1, MPI_DOUBLE, 0, 0, MPI_COMM_WORLD);
			MPI_Send (&m[loop], 1, MPI_DOUBLE, 0, 0, MPI_COMM_WORLD);
			MPI_Send (&c[loop], 1, MPI_DOUBLE, 0, 0, MPI_COMM_WORLD);

			MPI_Recv (&m_base, 1, MPI_DOUBLE, 0, 0, MPI_COMM_WORLD, MPI_STATUS_IGNORE);
			MPI_Recv (&c_base, 1, MPI_DOUBLE, 0, 0, MPI_COMM_WORLD, MPI_STATUS_IGNORE);
			MPI_Recv (&minimum_found, 1, MPI_INT, 0, 0, MPI_COMM_WORLD, MPI_STATUS_IGNORE);
		}
    	else {
			for(loop = 1; loop<mpi_size; loop++) {
				MPI_Recv (&pError, 1, MPI_DOUBLE, loop, 0, MPI_COMM_WORLD, MPI_STATUS_IGNORE);
				MPI_Recv (&m[loop-1] , 1, MPI_DOUBLE, loop, 0, MPI_COMM_WORLD, MPI_STATUS_IGNORE);
				MPI_Recv (&c[loop-1], 1, MPI_DOUBLE, loop, 0, MPI_COMM_WORLD, MPI_STATUS_IGNORE);

				if(pError < best_error) {
					best_error = pError;
					best_error_i = loop - 1;
				}
			}

			if(best_error < e_base) {
				e_base = best_error;
				m_base = m[best_error_i];
				c_base = c[best_error_i];
			}
		
			else {
				minimum_found = 1;
			}
			
			for(loop = 1; loop<mpi_size; loop++) {
				MPI_Send (&m_base, 1, MPI_DOUBLE, loop, 0, MPI_COMM_WORLD);
				MPI_Send (&c_base, 1, MPI_DOUBLE, loop, 0, MPI_COMM_WORLD);
				MPI_Send (&minimum_found, 1, MPI_INT, loop, 0, MPI_COMM_WORLD);

			}
		}
  }

  if(mpi_rank == 0) {
	  printf ("minimum m,c is %lf, %lf with error %lf\n", m_base, c_base, e_base);
  }

  MPI_Finalize();	// terminates all MPI execution environment

  // timer stops here
  clock_gettime(CLOCK_MONOTONIC, &timer_stop);
  timer_calc(&timer_start, &timer_stop, &time_taken_for_execution);
  
  // output of time taken for execution  is displayed
  printf("\n\n===============================================================================\n");
  printf("!! TIME TAKEN FOR EXECUTION !! \n");
  printf("===============================================================================\n\n");
  printf("Nanoseconds: %lld\n", time_taken_for_execution); 
  printf("Seconds: %0.9lf\n", ((time_taken_for_execution/1.0e9))); 
  printf("Minutes: %0.4lf\n", ((time_taken_for_execution/1.0e9)/60));
  printf("Hours: %0.2lf\n", ((time_taken_for_execution/1.0e9)/3600)); 
  
  return 0;
}

